# Lists in Python

# Lists hold information and can hold values like integers and strings

# Make a simple list of colors and then print them out
colors = ['blue', 'green', 'red', 'purple', 'yellow', 'black']
print(colors)

# You can also pick out specific elements of a list. Let's access purple
# Note: items in a list start at 0, therefore, purple would be spot # 3 in the list
print(colors[3])

