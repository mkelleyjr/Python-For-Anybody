# An example of F strings in Python
# F String = "formatted string literal" 
# F strings are easy to read with variables being inside {}
# F strings are evaluated at runtime

Name = "Michael"
Age = 33

# Use f and {} for the f strings
# f or F can be used. Both are valid
print(f"Hello, {Name}. You are {Age}.")

# F string math example
print(f"{5*5}")